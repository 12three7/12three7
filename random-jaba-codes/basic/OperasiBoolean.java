package dasar;

public class OperasiBoolean {
	
	public static void main(String[] args) {
		/**
		 * not gate, and gate, dan or gate. outputnya bisa true atau false.
		 */
		boolean e = false;
		boolean f = true;
		System.out.println(e || f); // or
		System.out.println(e && f); // and
		System.out.println(!e); // not
	}
	
}
