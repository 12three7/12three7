package dasar;

public class Number {
	
	public static void main(String[] args) {
		
		/**
		 * tipe data number. integer, float dan teman temannya.
		 */
		byte a = 10;
		short b = 100;
		int c = 100000000;
		long d = 100000000000000000L;
		float e = 1000000000.632946929927492849f;
		double f = 10000000000000000.8647354826492927492;
		
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
		System.out.println(e);
		System.out.println(f);
	}
	
}
