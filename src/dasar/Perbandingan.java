package dasar;

public class Perbandingan {
	
	public static void main(String[] args) {
		/**
		 * berguna buat decision making & buat bikin 
		 * boolean expression.
		 * 
		 * berarti outputnya bisa true atau false.
		 */
		int a = 100;
		System.out.println(a > 100);
		System.out.println(a < 100);
		System.out.println(a >= 100);
		System.out.println(a <= 100);
		System.out.println(a == 100);
		System.out.println(a != 100);
	}
	
}
